        public Persona(bool isMale)
        {
            Male = isMale;
            FirstName = GetFirstName(isMale);
            LastName = GetLastName();
            FullName = GetFullName(isMale);
            DateOfBirth = GetDateOfBirth();
            Height = GetHeight();
            Weight = GetWeight(isMale);
        }

        /// <summary>
        /// Gets a random first name based on the gender.
        /// </summary>
        /// <param name="isMale">Specifies whether the name should be male or female.</param>
        /// <returns>A random first name based on the specified gender.</returns>
        public static string GetFirstName(bool isMale) => isMale ? _firstNameMaleList[_random.Next(0, _firstNameMaleList.Count)] : _firstNameFemaleList[_random.Next(0, _firstNameFemaleList.Count)];

        /// <summary>
        /// Gets a random last name from the available list.
        /// </summary>
        /// <returns>A random last name from the list.</returns>
        public static string GetLastName() => _lastNameList[_random.Next(0, _lastNameList.Count)];

        /// <summary>
        /// Gets a random full name (first and last name) based on the gender.
        /// </summary>
        /// <param name="isMale">Specifies whether the first name should be male or female.</param>
        /// <returns>A random full name (first name + last name) based on the gender.</returns>
        public static string GetFullName(bool isMale) => $"{GetFirstName(isMale)} {GetLastName()}";

        /// <summary>
        /// Gets a random date of birth formatted as MM/DD/YYYY. The year is random between 1960 and 2001 (resulting in an age of 19 to 50 years old).
        /// </summary>
        /// <returns>A string representing a random date of birth in MM/DD/YYYY format.</returns>
        public static string GetDateOfBirth()
        {
            int m = _random.Next(1, 13); // Month: 1 -> 12.
            int d = _random.Next(1, 29); // Day: 1 -> 28.
            int y = _random.Next(1960, 2001); // Year: 1960 -> 2001 (50 -> 19 years old).

            return string.Format("{0:D2}/{1:D2}/{2:D4}", m, d, y);
        }

        /// <summary>
        /// Gets a random height for an individual in the format "5' XX" where XX is random inches from 1 to 11.
        /// </summary>
        /// <returns>A string representing a random height in feet and inches.</returns>
        public static string GetHeight()
        {
            int i = _random.Next(1, 12); // Inches: 1 -> 11.

            return string.Format("5' {0:D2}\"", i);
        }

        /// <summary>
        /// Gets a random weight for an individual based on gender. The weight is random within a specified range.
        /// </summary>
        /// <param name="isMale">Specifies whether the weight should be for a male or female.</param>
        /// <returns>A string representing a random weight in pounds, e.g., "160lbs".</returns>
        private static string GetWeight(bool isMale) => $"{(isMale ? _random.Next(155, 200) : _random.Next(110, 180))}lbs";
